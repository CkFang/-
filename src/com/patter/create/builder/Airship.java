package com.patter.create.builder;

public class Airship {

	private String orbitaModule;
	private String engine;
	private String escapeTower;
	public String getOrbitaModule() {
		return orbitaModule;
	}
	public void setOrbitaModule(String orbitaModule) {
		this.orbitaModule = orbitaModule;
	}
	public String getEngine() {
		return engine;
	}
	public void setEngine(String engine) {
		this.engine = engine;
	}
	public String getEscapeTower() {
		return escapeTower;
	}
	public void setEscapeTower(String escapeTower) {
		this.escapeTower = escapeTower;
	}
	
	
}
